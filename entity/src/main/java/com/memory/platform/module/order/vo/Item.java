package com.memory.platform.module.order.vo;

public class Item {
	private Object value;
	private String name;
	
	
	

	public Item() {
		super();
	}
	
	
	
	public Object getValue() {
		return value;
	}



	public void setValue(Object value) {
		this.value = value;
	}



	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
}
